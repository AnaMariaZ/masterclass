public class Dimensiones {
    private int wight;
    private int height;
    private int length;

    public Dimensiones(int wight, int height, int length) {
        this.wight = wight;
        this.height = height;
        this.length = length;
    }

    public int getWight() {
        return wight;
    }

    public int getHeight() {
        return height;
    }

    public int getLength() {
        return length;
    }
}
